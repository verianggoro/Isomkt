    private void jButtonLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonLoginActionPerformed

        try {
            String sql = "SELECT * FROM tb_admin WHERE username = '" +jTextNama.getText()+ "' AND password = '" + jPasswordField1.getText()+ "'";
            Statement st = df.getConnection().createStatement();
            ResultSet rsLogin = st.executeQuery(sql);

            rsLogin.next();
            rsLogin.last(); //mengecek jumlah baris pada hasil query
            if (rsLogin.getRow()==1){
                JOptionPane.showMessageDialog(null, "Login Berhasil!");
                new Menu_utama().setVisible(true); //<-- BILA MAU DIARAHKAN KE Frame Menu Utama
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Maaf, Username / Password salah!");
                jPasswordField1.setText("");
                jPasswordField1.requestFocus();
            }
        } catch (SQLException e) {
        }

    }//GEN-LAST:event_jButtonLoginActionPerformed

